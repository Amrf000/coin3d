void
coin_flipfont(void)
{
  const int numchars = sizeof(coin_default2dfont) / 12;
  for (int i = 0; i < numchars; i++) {
    for (int j = 0; j < 12 / 2; j++)
      SbSwap(coin_default2dfont[i][j], coin_default2dfont[i][11-j]);
  }
}
